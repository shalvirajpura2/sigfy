from __future__ import annotations

import argparse
import json
import re
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

from app.config import BASE_DIR, settings  # noqa: E402
from app.generate import generate_draft  # noqa: E402
from app.retrieval import get_retriever  # noqa: E402

SAMPLES = json.loads(
    (BASE_DIR.parent / "eval" / "sample_emails.json").read_text(encoding="utf-8")
)


def _alnum(s: str) -> str:
    return re.sub(r"[^a-z0-9]", "", s.lower())


def grounded(quote: str, chunks: list[dict]) -> bool:
    """a quote is grounded if a long-enough span of it appears in some source."""
    q = _alnum(quote)
    if len(q) < 20:
        return False
    corpus = _alnum(" ".join(c["text"] for c in chunks))
    if q in corpus:
        return True
    return q[: max(40, int(len(q) * 0.7))] in corpus


def run(as_md: bool) -> None:
    r = get_retriever()
    out = print
    if as_md:
        print("# sample outputs\n")
        print(f"generated by `scripts/run_eval.py` · model `{settings.model}` · "
              f"dense retrieval: {r.dense_enabled}\n")

    for s in SAMPLES:
        chunks = r.search(f"{s['subject']}\n{s['body']}")
        res = generate_draft(s["subject"], s["sender"], s["body"], chunks, debug=True)
        top_docs = []
        for c in chunks:
            if c["document"] not in top_docs:
                top_docs.append(c["document"])

        if as_md:
            out(f"## {s['subject']}\n")
            out(f"**from:** {s['sender']}\n")
            out(f"> {s['body']}\n")
            out(f"**retrieved documents (ranked):** {', '.join(top_docs)}\n")
            out(f"**found:** {res.found} · **confidence:** {res.confidence}\n")
            out("**drafted reply:**\n")
            out("```\n" + (res.draft or "") + "\n```\n")
            if res.citations:
                out("**citations:**\n")
                for c in res.citations:
                    g = "grounded" if grounded(c.quote, chunks) else "unverified"
                    loc = " · ".join(x for x in [c.section, f"p.{c.page}" if c.page else ""] if x)
                    out(f"- **{c.document}** — {loc}  \n  _{c.quote}_  ({g})")
                out("")
            if res.notes:
                out(f"**notes:** {res.notes}\n")
            out("---\n")
        else:
            print(f"\n=== {s['id']} — {s['subject']}")
            print(f"  retrieved: {top_docs}")
            print(f"  expect_doc in top-k: {s['expect_doc'] in top_docs}")
            print(f"  found={res.found} confidence={res.confidence} model={res.model}")
            if res.draft:
                print("  draft:", res.draft[:200].replace("\n", " "), "...")
            for c in res.citations:
                g = "grounded" if grounded(c.quote, chunks) else "UNVERIFIED"
                print(f"  cite: {c.document} | {c.section} | p.{c.page} [{g}]")


if __name__ == "__main__":
    ap = argparse.ArgumentParser()
    ap.add_argument("--md", action="store_true", help="emit markdown report")
    run(ap.parse_args().md)
